#include <stdio.h>

int main() {
    int ages[] = {20, 22, 18, 35, 48, 26, 87, 70};
    int length = sizeof(ages) / sizeof(ages[0]); 
    int sum = 0;
    int i;

    for (i = 0; i < length; i++) {
        sum += ages[i];
    }

    float average = (float) sum / length;
    printf("Average age: %.1f\n", average);

    return 0;
}
